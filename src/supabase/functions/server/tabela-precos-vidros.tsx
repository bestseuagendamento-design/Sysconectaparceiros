/**
 * FunÃ§Ã£o helper para converter a estrutura em formato plano
 */
export function getTabelaPlanar() {
  return [
    ...TABELA_VIDROS_COMPLETA.monoliticos,
    ...TABELA_VIDROS_COMPLETA.temperados,
    ...TABELA_VIDROS_COMPLETA.laminados,
    ...TABELA_VIDROS_COMPLETA.impressos,
    ...TABELA_VIDROS_COMPLETA.controle_solar,
    ...TABELA_VIDROS_COMPLETA.especiais,
  ];
}

/**
 * FunÃ§Ã£o para buscar vidro por cÃ³digo
 */
export function buscarVidroPorCodigo(codigo: string) {
  const todosVidros = getTabelaPlanar();
  return todosVidros.find(v => v.codigo === codigo);
}

/**
 * FunÃ§Ã£o para filtrar vidros por categoria
 */
export function buscarVidrosPorCategoria(categoria: string) {
  const todosVidros = getTabelaPlanar();
  return todosVidros.filter(v => v.categoria === categoria);
}

/**
 * EstatÃ­sticas da tabela
 */
export function getEstatisticasTabela() {
  const todosVidros = getTabelaPlanar();
  
  return {
    total: todosVidros.length,
    por_categoria: {
      comum: TABELA_VIDROS_COMPLETA.monoliticos.length,
      seguranca: TABELA_VIDROS_COMPLETA.temperados.length + TABELA_VIDROS_COMPLETA.laminados.length,
      decorativo: TABELA_VIDROS_COMPLETA.impressos.length,
      tecnico: TABELA_VIDROS_COMPLETA.controle_solar.length + TABELA_VIDROS_COMPLETA.especiais.filter(v => v.categoria === 'tecnico').length,
      estetico: TABELA_VIDROS_COMPLETA.especiais.filter(v => v.categoria === 'estetico').length,
    },
    valor_total_estoque: todosVidros.reduce((acc, v) => acc + v.preco, 0),
  };
}
